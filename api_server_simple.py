@app.get("/v1/turboquant/stats")
async def get_stats():
    """Get TurboQuant statistics."""
    global compressor

    if compressor is None:
        compressor = KVCacheCompressor(bit_width=3)

    stats = compressor.compute_memory_stats(seq_len=4096, n_heads=8, head_dim=128)

    return {
        "enabled": True,
        "bit_width": 3,
        "compression_ratio": stats["compression_ratio"],
        "memory_saved_gb": stats["memory_saved_gb"],
        "kv_cache_entries": kv_cache.seq_len if kv_cache else 0,
    }


@app.post("/v1/chat/completions")
async def chat_completions(request: ChatRequest):
    """Chat completions endpoint."""
    prompt = format_messages(request.messages)

    if request.stream:
        # Streaming response
        async def stream_generator():
            completion_id = f"chatcmpl-{uuid.uuid4().hex[:12]}"
            created = int(time.time())

            # Initial chunk
            initial = {
                "id": completion_id,
                "object": "chat.completion.chunk",
                "created": created,
                "model": request.model,
                "choices": [{"index": 0, "delta": {"role": "assistant"}, "finish_reason": None}],
            }
            yield f"data: {json.dumps(initial)}\n\n"

            # Generate and stream
            response = generate_response(prompt, request.max_tokens, request.turboquant_bits)
            words = response.split()

            for word in words:
                chunk = {
                    "id": completion_id,
                    "object": "chat.completion.chunk",
                    "created": created,
                    "model": request.model,
                    "choices": [
                        {"index": 0, "delta": {"content": word + " "}, "finish_reason": None}
                    ],
                }
                yield f"data: {json.dumps(chunk)}\n\n"
                time.sleep(0.05)  # Simulate streaming delay

            # Final chunk
            final = {
                "id": completion_id,
                "object": "chat.completion.chunk",
                "created": created,
                "model": request.model,
                "choices": [{"index": 0, "delta": {}, "finish_reason": "stop"}],
            }
            yield f"data: {json.dumps(final)}\n\n"
            yield "data: [DONE]\n\n"

        return StreamingResponse(stream_generator(), media_type="text/event-stream")

    else:
        # Non-streaming
        response_text = generate_response(prompt, request.max_tokens, request.turboquant_bits)

        return {
            "id": f"chatcmpl-{uuid.uuid4().hex[:12]}",
            "object": "chat.completion",
            "created": int(time.time()),
            "model": request.model,
            "choices": [
                {
                    "index": 0,
                    "message": {"role": "assistant", "content": response_text},
                    "finish_reason": "stop",
                }
            ],
            "usage": {
                "prompt_tokens": len(prompt.split()),
                "completion_tokens": len(response_text.split()),
                "total_tokens": len(prompt.split()) + len(response_text.split()),
            },
        }


