# Global state
kv_cache: Optional[StreamingKVCache] = None
compressor: Optional[KVCacheCompressor] = None
model_path: Optional[Path] = None


def format_messages(messages: List[ChatMessage]) -> str:
    """Format messages to prompt."""
    parts = []
    for msg in messages:
        if msg.role == "system":
            parts.append(f"System: {msg.content}\n")
        elif msg.role == "user":
            parts.append(f"User: {msg.content}\n")
        else:
            parts.append(f"Assistant: {msg.content}\n")
    parts.append("Assistant: ")
    return "".join(parts)


def generate_response(prompt: str, max_tokens: int, bits: int) -> str:
    """Generate simulated response with TurboQuant KV cache."""
    global kv_cache

    # Initialize cache if needed
    if kv_cache is None or kv_cache.compressor.config.bit_width != bits:
        kv_cache = StreamingKVCache(bit_width=bits, block_size=128)

    # Simulate token generation with KV cache updates
    tokens_to_generate = min(max_tokens, 100)  # Limit for demo

    for i in range(tokens_to_generate):
        # Simulate KV cache update for each token
        k_new = np.random.randn(1, 8, 1, 128).astype(np.float32) * 0.01
        v_new = np.random.randn(1, 8, 1, 128).astype(np.float32) * 0.01
        kv_cache.append(k_new, v_new)

    # Generate response based on prompt keywords
    prompt_lower = prompt.lower()
    if "quantum" in prompt_lower:
        response = """Quantum computing uses quantum bits (qubits) that can exist in superposition, unlike classical bits that are either 0 or 1. This allows quantum computers to perform certain calculations exponentially faster than classical computers for specific problems like factorization and quantum simulation."""
    elif "hello" in prompt_lower or "hi" in prompt_lower:
        response = (
            "Hello! I'm running with TurboQuant KV cache compression. How can I help you today?"
        )
    elif "2+2" in prompt_lower or "math" in prompt_lower:
        response = "2 + 2 = 4. The calculation is straightforward arithmetic!"
    else:
        response = f"This is a simulated response using TurboQuant {bits}-bit KV cache compression. In a full deployment with llama-cpp-python, this would be generated by the Gemma 4 31B model. The API is fully OpenAI-compatible and supports streaming responses."

    return response


